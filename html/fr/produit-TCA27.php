					<a name="features"></a><h4>System description</h4>
<p>System allows a tremendous improvement of security level during staring period of radio remote controls.</p>
<p>A typical accident with radio remote control is the miss use of a transmitter that does not correspond to the receiver of the application, <strong>the operator can by mistake confuse one transmitter with another</strong>.</p>
<p>It is then strongly recommended to fit radio remote control with an <strong>identification unit</strong> which ensure during the starting period a 100% compatibility or correspondence between transmitter and receiver.</p>
<p>JMei has <strong>developed a system (TCA27)</strong> which allows a secure operation of all functions <strong>without using an infra sources identification sensor</strong>.</p>
					<a name="specifications"></a><h4>Specifications</h4>
<p>The TCA27 system consist of the installation of a beacon made up of 4 (four) different lighting LED colours (red, yellow, green , blue) in a visible localisation and in the close environment of the operator (close to the mobile machine, overhead crane or tower crane , or any kind of mobile radio remote controlled equipment) .</p>
<p>This beacon is <strong>controlled and operated by the radio remote control itself, on the same frequency</strong> and address as used by the radio control equipment.</p>
<p>During the starting period, the beacon generated a two colours code, <strong>this code is random generated and will be different at each start up</strong>.</p>
<p>On the transmitter 4 push bottom are coloured  with four different colours,( red, yellow, green , blue) for joy stick operation, colours to be confirmed correspond to left right down and up functions.</p>
<p>Thus the operator confirms, the colour flashing or displayed by the beacon, by pressing the corresponding push coloured  button, two colours confirmation is necessary to get the operating permission, this operation can take 5 seconds max.</p>
<p>he colour code foreseen with two colour confirmation can be extended to four if necessary.</p>
